class Calculator {
  //La calculadora debe tener 4 funciones sumar restar dividir y multiplicar
  sumar(num1, num2) {
    return num1 + num2;
  }
  restar(num1, num2) {
    return num1 - num2;
  }
  dividir(num1, num2) {
    return num1 / num2;
  }
  multiplicar(num1, num2) {
    return num1 * num2;
  }
}
